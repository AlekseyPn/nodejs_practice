const os = require("os");

// Operation system

// platform
console.log(os.platform());
// architecture
console.log(os.arch());
// machine info
console.log(os.cpus());
// free memory
console.log(os.freemem());
// memory count
console.log(os.totalmem());
// home dir
console.log(os.homedir());
// system uptime
console.log(os.uptime());